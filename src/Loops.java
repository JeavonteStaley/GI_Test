public class Loops {
    public static void main(String[] args) {
        for(int i = 1; i <= 10; i++){
            System.out.println("Loop #: " + i);
        }
        System.out.println("---------------------------");
        for (int x = 1; x <= 10; ++x){
            System.out.println("Loop #: " + x);
        }
    }
}
